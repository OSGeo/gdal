 'WGS84_SRS_4326' ,
 4326,
 -180.0,
 5000000.0,
 -90.0,
 5000000,
 0,
 1000,
 0,
 1000,
 'GCS_WGS_1984',
 'EPSG4326',
     ?,
     ?) ;

