-- create an EPSG standard srs for NAD83
-- change the schema from db2gse to SYSPROC for DB2 on z/OS
call db2gse.ST_Create_srs(
 'NAD83_SRS_4269' ,
 4269,
 -180.0,
 5000000.0,
 -90.0,
 5000000,
 0,
 1000,
 0,
 1000,
 'GCS_NORTH_AMERICAN_1983',
 'EPSG4269',
     ?,
     ?) ;

